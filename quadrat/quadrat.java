package cat.quadrat;

public class quadrat {
    String color;
    float side, area;

    public String getColor() {
        return this.color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public float getSide(){
        return this.side;
    }

    public void setSide(float side) {
        this.side = side;
    }

    public float getArea(){
        return this.area;
    }

    public void setArea(float area) {
        this.area = area;
    }
}

