package cat.cat;

public class cat {
    String race;
    float height, width;

    public String getRace() {
        return this.race;
    }

    public void setRace(String race) {
        this.race = race;
    }

    public float getHeight(){
        return this.height;
    }

    public void setHeight(float height) {
        this.height = height;
    }

    public float getWidth(){
        return this.width;
    }

    public void setWidth(float width) {
        this.width = width;
    }
}

