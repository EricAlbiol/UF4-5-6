package cat.cotxe;

public class cotxe {
    String marca;
    float height, width;

    public String getMarca() {
        return this.marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public float getHeight(){
        return this.height;
    }

    public void setHeight(float height) {
        this.height = height;
    }

    public float getWidth(){
        return this.width;
    }

    public void setWidth(float width) {
        this.width = width;
    }
}

